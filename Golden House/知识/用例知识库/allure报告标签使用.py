# @allure.issue 记录用例的问题(超链接),@allure.description 用例的描述
# @allure.severity 用例的优先级,@allure.tag 用例的标记,@allure.testcase 记录用例的地址(超链接)
# @allure.description_html 用例的描述的网址(超链接),若存在description_html则@allure.description不显示
